//清除空格
const trim = (str, type) => {
    type = type || 1
    switch (type) {
        case 1: //全部
            return str.replace(/\s+/g, "");
        case 2: //前后
            return str.replace(/(^\s*)|(\s*$)/g, "");
        case 3: //开头
            return str.replace(/(^\s*)/g, "");
        case 4: //结尾
            return str.replace(/(\s*$)/g, "");
        default:
            return str;
    }
}

//密码强度
const checkPwd = (str) => {
    var Lv = 0;
    if (str.length < 6) {
        return Lv
    }
    if (/[0-9]/.test(str)) {
        Lv++
    }
    if (/[a-z]/.test(str)) {
        Lv++
    }
    if (/[A-Z]/.test(str)) {
        Lv++
    }
    if (/[\.|-|_]/.test(str)) {
        Lv++
    }
    return Lv;
}

// 判断键名是否相等
const isObjectEqual = (a, b) => {
    var aProps = Object.getOwnPropertyNames(a);
    var bProps = Object.getOwnPropertyNames(b);

    if (aProps.length !== bProps.length) {
        return false;
    }

    for (var i = 0; i < aProps.length; i++) {
        var propName = aProps[i];

        if (a[propName] !== b[propName]) {
            return false;
        }
    }
    return true;
}